# Comprehensive demo for loretta compiler

# Variables and arithmetic
x = 10
y = 3
print(x + y)       # 13
print(x - y)       # 7
print(x * y)       # 30
print(x // y)      # 3

# Strings
name = "loretta"
print("Hello, " + name + "!")

# Conditionals
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")

# While loop
i = 0
while i < 3:
    print(i)
    i = i + 1

# Boolean operations
a = True
b = False
print(a and b)     # False
print(a or b)      # True

# Built-in functions
print(len(name))   # 7
print(str(42))     # 42
