# Test basic types without control flow
print("Testing integers:")
print(10 + 5)
print(10 - 5)
print(10 * 5)

print("Testing floats:")
print(3.14 * 2)

print("Testing strings:")
s = "Hello"
print(s)
print(len(s))
print(s + " World")

print("Testing lists:")
nums = [1, 2, 3]
print(nums)
print(len(nums))

print("Testing tuples:")
t = (1, 2, 3)
print(t)

print("Testing dicts:")
d = {"a": 1, "b": 2}
print(d)

print("Done!")
